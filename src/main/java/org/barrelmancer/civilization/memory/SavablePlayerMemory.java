package org.barrelmancer.civilization.memory;

public class SavablePlayerMemory {
    private int thirst = 100;
    private int age = 0;

    public int getThirst() {
        return thirst;
    }

    public void setThirst(int thirst) {
        this.thirst = thirst;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
